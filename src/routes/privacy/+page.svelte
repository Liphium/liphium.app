		<h1>4. Data Security:</h1>
		<p>
			We prioritize the security of your information, so here are some methods we use to keep your
			information secure. We are committed to rigorously test these methods to ensure their
			effectiveness and reliability and to improve them in the future in case there is room for
			improvement.
		</p>
		<ul class="bullet-list">
			<li>
				<strong>End-to-End Encryption:</strong> Liphium employs end-to-end encryption to secure the conversations
				between Liphium accounts. This means that the content of your messages, including texts, images,
				voice messages, and any other attached content, is encrypted on your device and can only be decrypted
				by the intended recipient's device or any one who has previously received access to the key for
				encryption in the conversation. This ensures that the content remains private and secure during
				transit and storage and reduces the risk of getting your data to leak.
			</li>
			<li>
				<strong>Profile-based Encryption:</strong> Liphium incorporates encryption for any content shared
				with friends collectively, specifically content shared not within a private conversation but
				directly with all or a limited group of friends, referred to as "profile data''. All profile
				data is encrypted using a unique profile key (generated when creating an account, see “1.2 Information
				we automatically generate”). When you send a friend request to someone, they receive this key,
				enabling them to decrypt and access the shared profile data. It's important to note that all
				profile data is publicly available, and the only protection it has is through this encryption
				key. Note that you are responsible for this key not being shared with other people than your
				friends.
			</li>
			<li>
				<strong>Good security practices:</strong> We don’t say state-of-the-art security practices here
				because security practices get better by the day. We try to keep up with the current state of
				securing your data and want to make sure it’s stored as well as we can. For this reason we use
				really strong passwords for all of our service providers that make Liphium work. We also try
				to limit the data these service providers can read. Read more below.
			</li>
			<li>
				<strong>Protection on every request.</strong> We are committed to never share any of your personal
				data with service providers even if they say they won’t do anything with it. For example, we
				currently have a custom protocol on top of HTTPS that is used to protect you from getting data
				leaked to Cloudflare (which is our DDoS protection and SSL provider). Cloudflare could technically
				read all the data sent to our servers, but with our custom protocol this is no longer possible
				unless Cloudflare decides to suddenly modify requests (we will fight back in case that happens).
			</li>
		</ul>
