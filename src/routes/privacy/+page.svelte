		<h1>6. Data Retention:</h1>
		<p>
			We retain personal information until we no longer need it to keep up the functionality of our
			services or until it is no longer required for legal purposes. There are generally different
			types of data on our platform that may be kept for different time intervals.
		</p>
		<ul class="bullet-list">
			<li>
				<strong>Data that needs to reach a destination.</strong> For example, when you send a message
				on our services it is usually only kept for one month or more if required to send the message
				to the desired recipient. If you then edit the same message it will extend the length of storage
				with another 30 days. We do this to make sure the things you send arrive at the desired location.
				Messages can also be stored longer than the 30 day period since it may take the server some time
				to delete it.
			</li>
			<li>
				<strong>Data that is stored until you delete your account.</strong> Some data is kept until you
				decide to get rid of it by either deleting your account or deleting it directly. For example,
				when setting a profile picture on our services we keep it until you get rid of it. It would be
				really weird if your profile picture would suddenly disappear, you know.
